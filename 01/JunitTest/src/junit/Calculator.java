package junit;

/**
 * 计算器类
 */
public class Calculator {

    /**
     * 加法操作
     * @param a
     * @param b
     * @return
     */

    public int add (int a , int b) {

        return a + b;
    }

    /**
     * 减法操作
     * @param a
     * @param b
     * @return
     */
    public int substract (int a , int b) {
        return a - b;
    }

}
